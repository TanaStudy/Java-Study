class Solution {
    public List<Integer> preorderTraversal(TreeNode root) {
		List<Integer> res = new ArrayList<>();
		Stack<TreeNode> stack = new Stack<>();

		while (root != null || !stack.isEmpty()){
			while (root != null){
				res.add(root.val);
				stack.add(root);
				root = root.left;
			}
			root = stack.pop();
			root = root.right;
		}
		return res;
    }
}
